package com.log4h.singletontrip.evaluation.domain;

public class EvaluationCheckVo {
	private String roomNm;
	private int reserveNo;
	private String reserveCheckinDate;
	private String tripTitle;
	private int tripNo;
	private String tripStartDate;
	private int evaluationCheck;
	public String getRoomNm() {
		return roomNm;
	}
	public void setRoomNm(String roomNm) {
		this.roomNm = roomNm;
	}
	public int getReserveNo() {
		return reserveNo;
	}
	public void setReserveNo(int reserveNo) {
		this.reserveNo = reserveNo;
	}
	public String getReserveCheckinDate() {
		return reserveCheckinDate;
	}
	public void setReserveCheckinDate(String reserveCheckinDate) {
		this.reserveCheckinDate = reserveCheckinDate;
	}
	public String getTripTitle() {
		return tripTitle;
	}
	public void setTripTitle(String tripTitle) {
		this.tripTitle = tripTitle;
	}
	public int getTripNo() {
		return tripNo;
	}
	public void setTripNo(int tripNo) {
		this.tripNo = tripNo;
	}
	public String getTripStartDate() {
		return tripStartDate;
	}
	public void setTripStartDate(String tripStartDate) {
		this.tripStartDate = tripStartDate;
	}
	public int getEvaluationCheck() {
		return evaluationCheck;
	}
	public void setEvaluationCheck(int evaluationCheck) {
		this.evaluationCheck = evaluationCheck;
	}
	
	
}
