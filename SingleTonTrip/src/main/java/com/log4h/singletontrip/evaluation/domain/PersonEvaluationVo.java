package com.log4h.singletontrip.evaluation.domain;

public class PersonEvaluationVo {
	private int personEvaluationNo;
	private int tripNo;
	private String memberId;
	private String personId;
	private int personEvaluationRating;
	private String personEvaluationContent;
	private String personEvaluationRegDate;
	public int getPersonEvaluationNo() {
		return personEvaluationNo;
	}
	public void setPersonEvaluationNo(int personEvaluationNo) {
		this.personEvaluationNo = personEvaluationNo;
	}
	public int getTripNo() {
		return tripNo;
	}
	public void setTripNo(int tripNo) {
		this.tripNo = tripNo;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getPersonId() {
		return personId;
	}
	public void setPersonId(String personId) {
		this.personId = personId;
	}
	public int getPersonEvaluationRating() {
		return personEvaluationRating;
	}
	public void setPersonEvaluationRating(int personEvaluationRating) {
		this.personEvaluationRating = personEvaluationRating;
	}
	public String getPersonEvaluationContent() {
		return personEvaluationContent;
	}
	public void setPersonEvaluationContent(String personEvaluationContent) {
		this.personEvaluationContent = personEvaluationContent;
	}
	public String getPersonEvaluationRegDate() {
		return personEvaluationRegDate;
	}
	public void setPersonEvaluationRegDate(String personEvaluationRegDate) {
		this.personEvaluationRegDate = personEvaluationRegDate;
	}
	
}
