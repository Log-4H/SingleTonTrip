package com.log4h.singletontrip.page.domain;

public class PostVo {
	private int postNo;
	private String memberId;
	private String postTitle;
	private String postContent;
	private String postImg;
	private String postRegDate;
	private String postModifyDate;
	private String postDeleteDate;
	private String postViewCount;
	public int getPostNo() {
		return postNo;
	}
	public void setPostNo(int postNo) {
		this.postNo = postNo;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getPostTitle() {
		return postTitle;
	}
	public void setPostTitle(String postTitle) {
		this.postTitle = postTitle;
	}
	public String getPostContent() {
		return postContent;
	}
	public void setPostContent(String postContent) {
		this.postContent = postContent;
	}
	public String getPostImg() {
		return postImg;
	}
	public void setPostImg(String postImg) {
		this.postImg = postImg;
	}
	public String getPostRegDate() {
		return postRegDate;
	}
	public void setPostRegDate(String postRegDate) {
		this.postRegDate = postRegDate;
	}
	public String getPostModifyDate() {
		return postModifyDate;
	}
	public void setPostModifyDate(String postModifyDate) {
		this.postModifyDate = postModifyDate;
	}
	public String getPostDeleteDate() {
		return postDeleteDate;
	}
	public void setPostDeleteDate(String postDeleteDate) {
		this.postDeleteDate = postDeleteDate;
	}
	public String getPostViewCount() {
		return postViewCount;
	}
	public void setPostViewCount(String postViewCount) {
		this.postViewCount = postViewCount;
	}
	
	
}
